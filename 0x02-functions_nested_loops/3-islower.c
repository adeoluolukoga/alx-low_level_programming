#include "main.h"

/**
 * _islower - Entry point
 * @c - input letter
 * Description - it checks if an input is a lowercase
 * Return 1 if true an 0 if not
 */

int _islower(int c)
{

	int r;

	r = _islower(c);

	if (r != 0)
	{
		_putchar(r + '0');

		return (1);
	}
	else
	{
		_putchar(r + '0');

		return (0);
	}

}
