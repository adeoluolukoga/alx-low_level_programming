#include "main.h"

/**
 * _islower - Entry point
 * @c - input letter
 * Description - it checks if an input is a lowercase
 * Return 1 if true an 0 if not
 */

int _islower(int c)
{

	int r;

	switch (_islower(c))
	{
		r = _islower(c);

		case _islower('o'):
			_putchar(r + '0');

		case _islower(108):
			_putchar(r + '0');

		case _islower('c'):
			_putchar(r + '0');

		case _islower('I'):
			_putchar(r + '0');

		case _islower('s'):
			_putchar(r + '0');

		case _islower(70):
			_putchar(r + '0');

		case _islower(20):
			_putchar(r + '0');

		case _islower('U'):
			_putchar(r + '0');

		case _islower('n'):
			_putchar(r + '0');

		case _islower(0):
			_putchar(r + '0');

		case _islower('H'):
			_putchar(r + '0');
	}

	if (r != 0)
	{
		return (1);
	}
	else
	{
		return (0);
	}

}
